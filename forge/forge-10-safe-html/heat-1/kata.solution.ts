// escapeHtml — 解答

const ENTITIES: Record<string, string> = {
  "&": "&amp;",
  "<": "&lt;",
  ">": "&gt;",
  '"': "&quot;",
  "'": "&#39;",
};

export function escapeHtml(input: string): string {
  // 1度の走査で置換するので & を先に処理する問題は起きない
  return input.replace(/[&<>"']/g, (ch) => ENTITIES[ch]);
}

